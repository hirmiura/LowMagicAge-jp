<c=ty>投射武器: </c>ライトクロスボウ、スリング、ヘビークロスボウ、ショートボウ、コンポジットショートボウ、ロングボウ、コンポジットロングボウ、ハンドクロスボウ、リピーティングクロスボウは投射武器である。ほとんどの投射武器は使用するために両手を必要とする（特定の武器の説明を参照）。特別に作られたコンポジットショートボウ、特別に作られたコンポジットロングボウ、スリングでない限り、キャラクターは投射武器によるダメージロールに戦力ボーナスを受けない。STRが低いことによるペナルティがある場合、そのペナルティは弓やスリングを使用する際のダメージロールに適用される。

<c=ty>弾: </c>投射武器は、矢 (弓の場合)、ボルト (クロスボウの場合)、またはスリング弾 (スリングの場合) の弾を使用します。<z> 弓を使用する場合、キャラクターはフリーアクションとして弾を引き出すことができます。 クロスボウとスリングにはリロードのためのアクションが必要です。</z> 一般に、標的に命中した弾は破壊されるか役に立たなくなりますが、命中しなかった通常の弾は破壊または紛失する可能性が 50% あります。

