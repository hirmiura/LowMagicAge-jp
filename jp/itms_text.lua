<h2 c=ty>武器の特性</h2>
あなたが使う武器は、あなたが何者であるかを物語っている。おそらく近接武器と投射武器の両方を装備したいだろう。もし両方を装備する余裕がないのであれば、自分にとってどちらが重要かを判断しよう。

どのサイズの武器を選ぶかによって、その武器の振り方（片手か両手か）と、その武器で与えられるダメージの大きさが決まる。両手武器は片手武器よりも大きなダメージを与えるが、両手武器を振り回すと盾を使えなくなるので、トレードオフとなる。

熟練できる武器の数はクラスと種族によって異なる。また、適切な特技を選択することで、さらに多くの武器に習熟することができる。

優れた武器は通常、劣った武器よりも高価だが、高価だからといって必ずしも優れているとは限らない。例えば、レイピアはロングソードよりも高価である。ウェポンフィネスの特技を持つ器用なローグにとって、レイピアは素晴らしい武器である。一般的なファイターにはロングソードの方が良い。


武器を選ぶ際には、次のような資質を考慮する必要がある。:

<b c=ty>コスト:</b> この値は武器の金貨（gp）単位でのコストである。このコストには武器に付随する雑多な装備も含まれる。


<b c=ty>ダメージ:</b>ヒット時に武器が与えるダメージ。


<b c=ty>ダメージタイプ:</b>武器は与えるダメージの種類によって、打撃、貫通、斬撃に分類される。モンスターの中には、特定の種類の武器からの攻撃に対して耐性や免疫を持っているものもいる。

武器によっては複数の種類のダメージを与えるものがある。ある武器が2つのタイプを持つ場合、その武器が与えるダメージは1つのタイプともう1つのタイプの半々ではなく、すべてが両方のタイプである。したがって、クリーチャーがそのような武器からのダメージを無視するには、両方のタイプのダメージに対する免疫を持っていなければならない。

それ以外の場合、武器は2種類のダメージのどちらかを与えることができる。ダメージの種類が重要な状況では、使い手はそのような武器でどちらの種類のダメージを与えるかを選ぶことができる（ゲーム内で自動的に最適なものを選ぶ）。


<b c=ty>クリティカル:</b> あなたのキャラクターがクリティカルヒットを出したら、そのクリティカル倍率で示されるダメージを2回、3回、または4回ロールし（各ロールに適用可能なすべての修正を使用する）、すべての結果を合計する。

<b>例外:</b> 武器の通常ダメージ以上の追加ダメージは、クリティカルヒットを出しても乗算されない。

例えば:
5%/x2(or x2):この武器は自然出目が20でクリティカルヒットとなり、クリティカルヒット時には2倍のダメージを与える。
