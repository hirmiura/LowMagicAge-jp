<c=ty b>軽、片手、両手の近接武器: </c>この呼称は、戦闘において武器を振るのにどれだけの労力を要するかを示す尺度である。近接武器を、その武器のサイズカテゴリのキャラクターが振るったときに、軽武器、片手武器、両手武器のどれとみなされるかを示す。

<c=ty>軽: </c>軽武器は片手武器よりもオフハンドで使いやすく、組みつき中でも使える。軽武器は片手で使用する。軽武器を片手で使う場合、近接攻撃のダメージロールに使い手のSTRボーナス（もしあれば）を加え、片手で使わない場合は使い手のSTRボーナスの2分の1を加える。両手を使って軽武器を振るう場合、ダメージにアドバンテージは与えられない。STRボーナスは、その武器が使い手のメインハンドにのみ握られているものとして適用される。

非武装攻撃は常に軽武器とみなされる。

<c=ty>片手: </c>片手武器はメインハンドでもオフハンドでも使用できる。片手武器による近接攻撃のダメージロールには、片手武器がメインハンドで使用されている場合はその使用者のSTRボーナスを、オフハンドで使用されている場合はその使用者のSTRボーナスの1/2を加える。近接戦闘中に片手武器を両手で使用する場合、そのキャラクターのSTRボーナスの1/2倍をダメージロールに加える。

<c=ty>両手: </c>両手持ちの近接武器を効果的に使用するには両手が必要である。そのような武器による近接攻撃のダメージロールには、キャラクターのSTRボーナスの1～1/2倍を適用する。 


<c=ty b>ウェポンサイズ: </c>すべての武器にはサイズカテゴリーがある。この指定は、その武器が設計されたクリーチャーのサイズを示す。<z>

武器のサイズカテゴリーは、オブジェクトとしてのサイズと同じではありません。その代わり、武器のサイズカテゴリーは意図する使い手のサイズに連動する。一般的に、軽武器は使い手より2サイズ小さいオブジェクトであり、片手武器は使い手より1サイズ小さいオブジェクトであり、両手武器は使い手と同じサイズカテゴリのオブジェクトである。

