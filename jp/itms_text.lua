武器を使うのにどれだけの努力が必要かという尺度（その武器が特定の使い手にとって軽、片手、両手のどれに指定されているか）は、使い手のサイズとその武器がデザインされたクリーチャーのサイズとの差がサイズカテゴリーごとに1段階ずつ変更される。この変更によって武器の指定が軽、片手、両手以外のものに変更される場合、そのクリーチャーはその武器をまったく振るうことができない。


