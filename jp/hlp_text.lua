A: 良いご指摘ですね。装填のためにクリックするのはわずらわしくて、その都度「機会攻撃を誘発する」とポップアップ警告が出るのはよくないという点が理由に挙げられます。そのため、装填という行動は《高速装填》の特技とともに削除しました。

